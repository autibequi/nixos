#!/bin/bash
# Generate self-signed certs for local development
# Uses pre-generated dummy certs if openssl is not available

CERT_DIR="$(dirname "$0")/certs"
mkdir -p "$CERT_DIR"

DOMAIN="local.estrategia-sandbox.com.br"
SUBDOMAINS="*.local.estrategia-sandbox.com.br"

# Try openssl first
if command -v openssl &> /dev/null; then
    echo "Using openssl to generate certificates..."
    openssl req -x509 -nodes -days 365 \
        -newkey rsa:2048 \
        -keyout "$CERT_DIR/privkey.pem" \
        -out "$CERT_DIR/fullchain.pem" \
        -subj "/CN=$DOMAIN" \
        -addext "subjectAltName=DNS:$DOMAIN,DNS:$SUBDOMAINS,DNS:localhost"
else
    echo "openssl not found, using pre-generated dummy certificates for development..."

    # Pre-generated 2048-bit RSA key (valid for local development)
